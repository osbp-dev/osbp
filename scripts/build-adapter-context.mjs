#!/usr/bin/env node
// Assembles the AI-ready adapter implementation context. The output is checked
// in so an agent can be pointed at one deterministic file plus target API docs.
//   build:  node scripts/build-adapter-context.mjs
//   check:  node scripts/build-adapter-context.mjs --check
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { basename, join } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = fileURLToPath(new URL("..", import.meta.url));
const outputPath = join(repoRoot, "dist/osbp-adapter-context.md");

const sections = [
  markdownSection(
    "v0.1.0 Specification",
    readText("docs/spec/v0.1.0/README.md")
  ),
  markdownSection(
    "v0.1.0 Schema Notes",
    readText("docs/spec/v0.1.0/schema.md")
  ),
  schemaSection(),
  codeSection(
    "BookingAdapter Interface",
    "ts",
    readText("packages/osbp-core/src/index.ts")
  ),
  conformanceSection(),
  starterSection()
];

const assembled = `# OSBP Adapter Context Bundle

This generated bundle gives a developer or AI coding agent the OSBP v0.1.0 adapter contract, published schemas, conformance checklist, and starter skeleton in one deterministic file.

Generated by \`scripts/build-adapter-context.mjs\`. Do not edit this file by hand.

${sections.join("\n\n")}
`;

if (process.argv.includes("--check")) {
  const current = readFileSync(outputPath, "utf8");
  if (current !== assembled) {
    console.error("dist/osbp-adapter-context.md is stale. Run `node scripts/build-adapter-context.mjs` and commit the result.");
    process.exitCode = 1;
  } else {
    console.log("dist/osbp-adapter-context.md is current");
  }
} else {
  mkdirSync(join(repoRoot, "dist"), { recursive: true });
  writeFileSync(outputPath, assembled);
  console.log("wrote dist/osbp-adapter-context.md");
}

function readText(path) {
  return readFileSync(join(repoRoot, path), "utf8").trim();
}

function markdownSection(title, body) {
  return `## ${title}

${body}`;
}

function codeSection(title, language, body) {
  return `## ${title}

\`\`\`${language}
${body}
\`\`\``;
}

function schemaSection() {
  const schemaPaths = [
    "schemas/v0.1.0/booking-mandate.schema.json",
    "schemas/v0.1.0/problem.schema.json",
    "schemas/v0.1.0/result-envelope.schema.json",
    "schemas/v0.1.0/tool-inputs.schema.json"
  ];

  return `## Published JSON Schemas

${schemaPaths.map((schemaPath) => codeSection(basename(schemaPath), "json", readText(schemaPath))).join("\n\n")}`;
}

function conformanceSection() {
  const requirements = extractConformanceRequirements(readText("packages/osbp-conformance/src/index.ts"));
  return `## Conformance Requirements

${requirements.map((requirement) => `- \`${requirement.id}\`: ${requirement.requirement}`).join("\n")}`;
}

function starterSection() {
  const files = [
    ["packages/adapter-starter/package.json", "json"],
    ["packages/adapter-starter/tsconfig.json", "json"],
    ["packages/adapter-starter/README.md", "md"],
    ["packages/adapter-starter/src/index.ts", "ts"],
    ["packages/adapter-starter/src/conformance-target.ts", "ts"],
    ["packages/adapter-starter/src/cli.ts", "ts"]
  ];

  return `## Adapter Starter Skeleton

${files.map(([path, language]) => codeSection(path, language, readText(path))).join("\n\n")}`;
}

function extractConformanceRequirements(source) {
  const matches = [];
  for (const match of source.matchAll(/\{\s*id: "([^"]+)",\s*requirement: "([^"]+)",\s*spec_ref:/g)) {
    matches.push({
      index: match.index ?? 0,
      id: match[1],
      requirement: match[2]
    });
  }
  for (const match of source.matchAll(/problemCheck\("([^"]+)",\s*"[^"]+",\s*"([^"]+)"/g)) {
    matches.push({
      index: match.index ?? 0,
      id: match[1],
      requirement: match[2]
    });
  }

  return matches
    .sort((left, right) => left.index - right.index)
    .map(({ id, requirement }) => ({ id, requirement }));
}
